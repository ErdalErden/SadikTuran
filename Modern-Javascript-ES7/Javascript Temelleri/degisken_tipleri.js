// Değişken Tipleri

// A -Primitive Tipler

// String
let firstName='Çınar';
console.log(typeof firstName);

// Number
let age=20;
let money=100.5;
console.log(typeof age);
console.log(typeof money);

// Boolean
let isActive=true;
console.log(typeof isActive);

// null
let job=null;
console.log(typeof job);

// undefined
let car;
console.log(typeof car);

// B - Reference Tipler - Objects

// Array
let names = ['Ali','Ahmet','Can']
console.log(typeof names);

// Object
let address = {
    city:'Kocaeli',
    country:'Türkiye'
};
console.log(address);
console.log(typeof address);